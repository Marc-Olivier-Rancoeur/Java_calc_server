public class Calculatrice{
    public static int ADD(int chiffre1, int chiffre2){
        return chiffre1+chiffre2;
    }
    public static int SUB(int chiffre1, int chiffre2){
        return chiffre1-chiffre2;
    }
    public static int MUL(int chiffre1, int chiffre2){
        return chiffre1*chiffre2;
    }
    public static float DIV(int chiffre1, int chiffre2){
        return ((float)chiffre1)/((float)chiffre2);
    }
}
